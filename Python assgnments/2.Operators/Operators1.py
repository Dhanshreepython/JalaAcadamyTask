# 1. Write a function for arithmetic operators(+,-,*,/)
def addition(x,y):
    sum=x+y
    return sum
print("The sum is",addition(6,9))


def Subtraction(x,y):
    sub=x-y
    return sub
print("The sub is",Subtraction(6,9))


def Multiplication(x,y):
    mul=x*y
    return mul
print("The mul is",Multiplication(6,9))


def Division(x,y):
    div=x/y
    return div
print("The div is",Division(6,9))
